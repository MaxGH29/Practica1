package Parte2;

public class Insumo {
	private String idProducto,prodcuto,isCategoria;

	public Insumo(String idProducto, String prodcuto, String isCategoria) {
		this.idProducto = idProducto;
		this.prodcuto = prodcuto;
		this.isCategoria = isCategoria;
	}

	public String getIdProducto() {
		return idProducto;
	}

	public void setIdProducto(String idProducto) {
		this.idProducto = idProducto;
	}

	public String getProdcuto() {
		return prodcuto;
	}

	public void setProdcuto(String prodcuto) {
		this.prodcuto = prodcuto;
	}

	public String getIsCategoria() {
		return isCategoria;
	}

	public void setIsCategoria(String isCategoria) {
		this.isCategoria = isCategoria;
	}
	
	
	
	

}
